              <span><font size="4"><p>Anesthesia is a unique medical intervention which does not itself offer any particular medical benefit and instead enables the performance of other medical interventions. The best anesthetic is therefore one with the lowest risk to the patient that still achieves the end points required to complete the other intervention. There are many different needs and goals of anesthesia. The goals (end points) are traditionally described as unconsciousness and amnesia, analgesia, and muscle relaxation. To reach multiple end points one or more drugs are commonly used (such as general anesthetics, hypnotics, sedatives, paralytics, narcotics, and analgesics), each of which serves a specific purpose in creating a safe anesthetic.<br>

The types of anesthesia are broadly classified into general anesthesia, sedation and regional anesthesia. General anesthesia refers to the suppression of activity in the central nervous system, resulting in unconsciousness and total lack of sensation. Sedation (or dissociative anesthesia) uses agents that inhibit transmission of nerve impulses between higher and lower centers of the brain inhibiting anxiety and the creation of long-term memories. Regional anesthesia renders a larger area of the body insensate by blocking transmission of nerve impulses between a part of the body and the spinal cord. It is divided into peripheral and central blockades. Peripheral blockade inhibits sensory perception within a specific location on the body, such as when a tooth is "numbed" or when a nerve block is given to stop sensation from an entire limb. Central blockades place the local anesthetic around the spinal cord (such as with spinal and epidural anesthesia) removing sensation to any area below the level of the block.<br>

There are both major and minor risks of anesthesia. Examples of major risks include death, heart attack and pulmonary embolism whereas minor risks can include postoperative nausea and vomiting and readmission to hospital. The likelihood of a complication occurring is proportional to the relative risk of a variety of factors related to the patient's health, the complexity of the surgery being performed and the type of anesthetic. Of these factors, the person's health prior to surgery (stratified by the ASA physical status classification system) has the greatest bearing on the probability of a complication occurring. Patients typically wake within minutes of an anesthetic being terminated and regain their senses within hours. One exception is a condition called long-term post-operative cognitive dysfunction, characterized by persistent confusion lasting weeks or months, which is more common in those undergoing cardiac surgery and in the elderly.<br>

The first public demonstration of general anesthesia was in 1846 by a Boston dentist named William T.G. Morton at the Massachusetts General Hospital. Dr. Morton gave an ether anesthetic for the removal of a neck tumor by surgeon John Collins Warren (the first editor of the New England Journal of Medicine and dean of Harvard Medical School). About a decade later, cocaine was introduced as the first viable local anesthetic. It wasn't until the 1930s that Dr. Harvey Cushing tied the stress response to higher mortality rates and began using local anesthetic for hernia repairs in addition to general anesthesia.</P></font></span>
		
